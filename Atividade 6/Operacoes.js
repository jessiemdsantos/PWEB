var
    num1,
    num2;

num1 = prompt("Informe o primeiro número");
num2 = prompt("Informe o segundo número");
alert("Soma = " + (parseFloat(num1) + parseFloat(num2)));
alert("Subtração = " + (parseFloat(num1) - parseFloat(num2)));
alert("Produto = " + (parseFloat(num1) * parseFloat(num2)));
alert("Divisão = " + (parseFloat(num1) / parseFloat(num2)));
alert("Resto = " + (parseFloat(num1) % parseFloat(num2)));